import SwiftUI

nonisolated struct MuscleZoneDefinition: Sendable {
    let id: String
    let muscle: MuscleGroup
    let points: [CGPoint]
    let hitPoints: [CGPoint]?

    init(id: String, muscle: MuscleGroup, points: [CGPoint], hitPoints: [CGPoint]? = nil) {
        self.id = id
        self.muscle = muscle
        self.points = points
        self.hitPoints = hitPoints
    }

    var effectiveHitPoints: [CGPoint] { hitPoints ?? points }
}

struct MuscleRegionShape: Shape {
    let normalizedPoints: [CGPoint]

    func path(in rect: CGRect) -> Path {
        guard normalizedPoints.count >= 3 else { return Path() }

        let scaled = normalizedPoints.map { pt in
            CGPoint(x: pt.x * rect.width, y: pt.y * rect.height)
        }

        var path = Path()
        path.move(to: scaled[0])

        let count = scaled.count
        for i in 0..<count {
            let p0 = scaled[(i - 1 + count) % count]
            let p1 = scaled[i]
            let p2 = scaled[(i + 1) % count]
            let p3 = scaled[(i + 2) % count]

            let next = scaled[(i + 1) % count]
            let cp1 = CGPoint(
                x: p1.x + (p2.x - p0.x) / 6.0,
                y: p1.y + (p2.y - p0.y) / 6.0
            )
            let cp2 = CGPoint(
                x: next.x - (p3.x - p1.x) / 6.0,
                y: next.y - (p3.y - p1.y) / 6.0
            )
            path.addCurve(to: next, control1: cp1, control2: cp2)
        }

        path.closeSubpath()
        return path
    }
}

